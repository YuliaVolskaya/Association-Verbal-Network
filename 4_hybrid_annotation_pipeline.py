    # Эталонные строки
    df_gold = pd.read_excel(GOLD_DATA_FILE)
    df_gold['key'] = df_gold['стимул'].astype(str) + '::' + df_gold['ассоциация'].astype(str)
    df_full['key'] = df_full['стимул'].astype(str) + '::' + df_full['ассоциация'].astype(str)
    gold_keys = set(df_gold['key'])
    mask_gold = df_full['key'].isin(gold_keys)
    df_gold_in_full = df_full[mask_gold].copy()
    df_to_predict = df_full[~mask_gold].copy()
