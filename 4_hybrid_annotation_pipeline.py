# 0. ПОДГОТОВКА ПРИЗНАКОВ И ЦЕЛЕВЫХ МЕТОК
def load_and_featurize(path):

    if not os.path.exists(path):
        raise FileNotFoundError(f"Файл {path} не найден. Пожалуйста, поместите его в рабочую директорию.")
    df = pd.read_excel(path)
    df_ML = pd.DataFrame()
    df_ML['стимул'] = df['стимул']
    df_ML['ассоциация'] = df['ассоциация']

    # Количество слов в ассоциации
    df_ML['assoc_word_count'] = df['ассоциация'].astype(str).apply(lambda x: len(x.strip().split()))

    # Числовые признаки
    cols_to_fix = ['Wu Palmer', 'Delta P', 'Dice Score', 'PMI', 'частота', 'Концептуальная частота']
    for col in cols_to_fix:
        df_ML[col] = pd.to_numeric(df.get(col, 0), errors='coerce').fillna(0)

    # Формальные и эпидигматические признаки
    epi_col = df['эпидигматика и формальная связь'].astype(str).str.lower()
    df_ML['feat_identical'] = epi_col.apply(lambda x: 1 if 'идентичные' in x else 0)
    df_ML['feat_formal_phonetic'] = epi_col.apply(lambda x: 1 if any(kw in x for kw in ['фонетическая', 'графическая', 'историческая']) else 0)
    df_ML['feat_epidigmatic'] = epi_col.apply(lambda x: 1 if 'эпидигматическая' in x else 0)
    df_ML['feat_grammatical_forms'] = epi_col.apply(lambda x: 1 if 'грамматическая' in x else 0)

    # Парадигматика (RuWordNet)
    para_col = df['RuWordNet'].astype(str).str.lower()
    df_ML['para_synonym'] = para_col.apply(lambda x: 1 if 'синоним' in x else 0)
    df_ML['para_antonym'] = para_col.apply(lambda x: 1 if 'антоним' in x else 0)
    df_ML['para_hippo'] = para_col.apply(lambda x: 1 if 'гипоним' in x or 'гипероним' in x else 0)
    df_ML['para_any'] = para_col.apply(lambda x: 1 if any(c.isalpha() for c in x) and x != 'nan' else 0)

    # Совпадение частей речи
    def check_pos_match(s_pos, a_pos):
        s_str, a_str = str(s_pos).strip(), str(a_pos).strip()
        if '+' in a_str: return 0
        s_first = s_str.split('/')[0]
        a_first = a_str.split('/')[0]
        s_clean = re.sub(r'[^\w]', '', s_first).upper()
        a_clean = re.sub(r'[^\w]', '', a_first).upper()
        return 1 if (s_clean == a_clean and s_clean != '') else 0
    df_ML['POS_match'] = df.apply(lambda row: check_pos_match(row['часть речи стимула'], row['часть речи ассоциации']), axis=1)

    # Семантическая группа и фрейм
    df_ML['sem_tag'] = df['общая сем.группа'].apply(lambda x: 1 if pd.notna(x) and str(x).strip() != '' else 0)
    fb_cols = ['FB_точные примеры', 'FB_пересечение', 'FB_ролевая связь']
    df_ML['frame_tag'] = df[fb_cols].apply(
        lambda row: 1 if any(pd.notna(x) and str(x).strip() != '' for x in row) else 0, axis=1
    )

    # Синтаксический признак (объединённый)
    syntax_list = [
        'атрибутивное: определительное', 'актантное', 'атрибутивное: обстоятельственное',
        'атрибутивное', 'атрибутивное: аппроксимативно-порядковое/количественное', 'служебное'
    ]
    df_ML['synt'] = df['Синтаксическая разметка'].astype(str).apply(
        lambda x: 1 if any(syn in x for syn in syntax_list) else 0
    )

    # Устойчивость (коллокация)
    df_ML['collocation'] = df['устойчивость'].astype(str).str.lower().apply(lambda x: 1 if 'коллокация' in x else 0)

    # Multi-label цели (типы связи)
    target_list = [
        'синтаксический', 'синтагматический', 'эпидигматический',
        'парадигматический', 'коллокативный', 'тематический',
        'составная', 'идентичные', 'формальный', 'грамматический (формы слова)',
        'не определена'
    ]
    target_column = 'тип связи'
    for t in target_list:
        clean_name = t.replace(' (формы слова)', '').replace(' ', '_')
        safe_col_name = f"target_{clean_name}"
        df_ML[safe_col_name] = df[target_column].astype(str).apply(
            lambda x: 1 if t.lower() in [tag.strip().lower() for tag in x.split('/')] else 0
        )
    return df_ML

print("Шаг 1: Генерация признаков")
df_ML = load_and_featurize(EIGHT_WORDS_FILE)

