# 2. ОБУЧЕНИЕ МОДЕЛИ BERT (если есть золотой стандарт)
feature_cols = [
    'feat_identical', 'feat_formal_phonetic', 'feat_epidigmatic', 'feat_grammatical_forms',
    'POS_match', 'para_synonym', 'para_antonym', 'para_hippo', 'para_any',
    'sem_tag', 'frame_tag', 'synt', 'collocation', 'Wu Palmer', 'sim_sbert'
]

if os.path.exists(GOLD_DATA_FILE):
    print("Шаг 3: Обучение модели BERT")
    gold_source_df = pd.read_excel(GOLD_DATA_FILE)
    gold_source_df = gold_source_df.dropna(subset=['стимул', 'ассоциация'])

    target_cols = [col for col in df_ML.columns if col.startswith('target_')]
    class_names = [col.replace('target_', '') for col in target_cols]

    # Проверка наличия признаков
    missing_feat = [f for f in feature_cols if f not in gold_source_df.columns]
    if missing_feat:
        raise KeyError(f"В эталонном файле отсутствуют признаки: {missing_feat}")

    train_df, val_df = train_test_split(gold_source_df, test_size=0.2, random_state=RANDOM_STATE)
    train_labels = train_df[target_cols].values.astype(np.float32)
    val_labels   = val_df[target_cols].values.astype(np.float32)

    scaler = StandardScaler()
    train_feat = scaler.fit_transform(train_df[feature_cols].values.astype(np.float32))
    val_feat   = scaler.transform(val_df[feature_cols].values.astype(np.float32))
    joblib.dump(scaler, SCALER_SAVE_PATH)

    tokenizer = AutoTokenizer.from_pretrained(BERT_MODEL_NAME)
    train_enc = tokenizer(train_df['стимул'].tolist(), train_df['ассоциация'].tolist(),
                          truncation=True, padding=True, max_length=64, return_tensors='pt')
    val_enc   = tokenizer(val_df['стимул'].tolist(), val_df['ассоциация'].tolist(),
                          truncation=True, padding=True, max_length=64, return_tensors='pt')

    class HybridDataset(torch.utils.data.Dataset):
        def __init__(self, encodings, features, labels):
            self.encodings = encodings
            self.features = features
            self.labels = labels
        def __getitem__(self, idx):
            item = {key: val[idx] for key, val in self.encodings.items()}
            item['features'] = torch.tensor(self.features[idx], dtype=torch.float)
            item['labels'] = torch.tensor(self.labels[idx], dtype=torch.float)
            return item
        def __len__(self):
            return len(self.labels)

    train_dataset = HybridDataset(train_enc, train_feat, train_labels)
    val_dataset   = HybridDataset(val_enc, val_feat, val_labels)

    class FocalLoss(nn.Module):
        def __init__(self, alpha=0.25, gamma=2.0):
            super().__init__()
            self.alpha, self.gamma = alpha, gamma
        def forward(self, inputs, targets):
            bce = nn.functional.binary_cross_entropy_with_logits(inputs, targets, reduction='none')
            pt = torch.exp(-bce)
            return (self.alpha * (1 - pt) ** self.gamma * bce).mean()

    class BertHybrid(nn.Module):
        def __init__(self, model_name, num_labels, num_features):
            super().__init__()
            self.bert = AutoModel.from_pretrained(model_name)
            self.dropout = nn.Dropout(0.4)
            self.classifier = nn.Linear(768 + num_features, num_labels)
            self.loss_fn = FocalLoss(0.25, 2.0)
        def forward(self, input_ids, attention_mask, features, labels=None, **kwargs):
            outputs = self.bert(input_ids=input_ids, attention_mask=attention_mask)
            pooled = self.dropout(outputs.pooler_output)
            combined = torch.cat((pooled, features), dim=1)
            logits = self.classifier(combined)
            loss = self.loss_fn(logits, labels) if labels is not None else None
            return SequenceClassifierOutput(loss=loss, logits=logits)

    device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
    model = BertHybrid(BERT_MODEL_NAME, len(target_cols), len(feature_cols))
    model.to(device)
    for param in model.parameters():
        param.data = param.data.contiguous()

    training_args = TrainingArguments(
        output_dir='./results',
        num_train_epochs=10,
        per_device_train_batch_size=8,
        per_device_eval_batch_size=16,
        eval_strategy="epoch",
        save_strategy="epoch",
        load_best_model_at_end=True,
        metric_for_best_model="f1_macro",
        learning_rate=2e-5,
        weight_decay=0.05,
        logging_steps=10,
        report_to="none"
    )

    def compute_metrics(eval_pred):
        logits, labels = eval_pred
        probs = torch.sigmoid(torch.tensor(logits)).numpy()
        preds = (probs >= 0.5).astype(int)
        return {'f1_macro': f1_score(labels, preds, average='macro', zero_division=0)}

    trainer = Trainer(
        model=model,
        args=training_args,
        train_dataset=train_dataset,
        eval_dataset=val_dataset,
        compute_metrics=compute_metrics
    )
    trainer.train()
    trainer.save_model(MODEL_SAVE_DIR)
    tokenizer.save_pretrained(MODEL_SAVE_DIR)
    torch.save(model.state_dict(), os.path.join(MODEL_SAVE_DIR, "pytorch_model_weights.bin"))

    # Оценка на валидации
    preds = trainer.predict(val_dataset)
    probs = torch.sigmoid(torch.tensor(preds.predictions)).numpy()
    y_pred = (probs >= 0.5).astype(int)
    print("\n=== Результаты на валидации ===")
    print(classification_report(val_labels, y_pred, target_names=class_names, zero_division=0))

    # Сохраняем обученную модель и scaler
    print(f"Модель сохранена в {MODEL_SAVE_DIR}")
