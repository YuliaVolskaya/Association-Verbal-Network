def hybrid_label(row):
    auto_str = str(row.get('auto_labels_final', '')).strip()
    bert_str = str(row.get('predicted_labels_final', '')).strip()

    if auto_str and auto_str != 'не определена':
        auto_tags = [t.strip() for t in auto_str.split('/')]
    else:
        auto_tags = []
    if bert_str and bert_str != 'не определена':
        bert_tags = [t.strip() for t in bert_str.split('/')]
    else:
        bert_tags = []

    if not bert_tags:
        return ' / '.join(sorted(auto_tags)) if auto_tags else 'не определена'

    tags = set(bert_tags)

    # 1. Удаляем идентичные, формальные, эпидигматические, грамматические
    tags.discard('идентичные')
    tags.discard('формальный')
    tags.discard('эпидигматический')
    tags.discard('грамматический')

    # 2. Парадигматика только при совпадении частей речи
    pos_match = row.get('POS_match', 0)
    if 'парадигматический' in tags and pos_match == 0:
        tags.discard('парадигматический')

    # 3. Коллокативный без синтаксического/синтагматического
    if 'коллокативный' in tags and 'синтаксический' not in tags and 'синтагматический' not in tags:
        if pos_match == 0:
            tags.add('синтагматический')
        else:
            tags.add('синтаксический')

    # 4. Исправление синтаксический/синтагматический по POS_match
    if pos_match == 1:
        if 'синтагматический' in tags:
            tags.discard('синтагматический')
            tags.add('синтаксический')
    else:
        if 'синтаксический' in tags:
            tags.discard('синтаксический')
            tags.add('синтагматический')

    # 5. Тематический + второй тег из авто-разметки
    if 'тематический' in tags:
        auto_set = set(auto_tags)
        if 'синтагматический' in auto_set and 'синтагматический' not in tags:
            tags.add('синтагматический')
        if 'синтаксический' in auto_set and 'синтаксический' not in tags:
            tags.add('синтаксический')
        if 'коллокативный' in auto_set and 'коллокативный' not in tags:
            tags.add('коллокативный')

    # 6. Обработка составных ассоциаций
    assoc_wc = row.get('assoc_word_count', 1)
    auto_has_syn_comp = ('синтаксический' in auto_tags) and ('составная' in auto_tags)

    if assoc_wc == 1:
        if 'составная' in tags:
            tags.discard('составная')
            if auto_has_syn_comp:
                tags.discard('синтаксический')
                tags.discard('синтагматический')
                tags.add('синтаксический')
                tags.add('составная')

    # 7. Добавление из авто-разметки пропущенные формальные, эпидигматические, грамматические, идентичные связи
    for cls in ['формальный', 'эпидигматический', 'грамматический', 'идентичные']:
        if cls in auto_tags and cls not in tags:
            tags.add(cls)

    # 8. Парадигматика из авто-размтеки (только при POS_match == 1)
    if 'парадигматический' in auto_tags and 'парадигматический' not in tags and pos_match == 1:
        tags.add('парадигматический')

    # 9. Если итоговый список пуст, используем авто-метки
    if not tags:
        if auto_tags:
            tags = set(auto_tags)
        else:
            return 'не определена'

    # Сортировка
    priority_order = {
        'синтаксический': 0, 'синтагматический': 1, 'коллокативный': 2,
        'тематический': 3, 'парадигматический': 4, 'эпидигматический': 5,
        'формальный': 6, 'грамматический': 7, 'идентичные': 8, 'составная': 9
    }
    sorted_tags = sorted(tags, key=lambda t: priority_order.get(t, 99))
    return ' / '.join(sorted_tags)

